-- Legacy Data 정리 (1회성 실행)
--
-- 1) 전화번호가 정상적인 번호 형식이 아닌 행 → 010-1234-1234 로 일괄 치환
-- 2) 신청자 이름(guest_name)에 들어 있는 ( ... ) 괄호 부분 제거
--
-- Supabase Dashboard → SQL Editor 에서 한 번 실행하면 됩니다.
-- ============================================

-- ============================================================
-- 1. 전화번호 정리
-- ============================================================
-- '미입력', 빈 문자열, 공백만, 또는 숫자/하이픈/공백/+ 외 다른 문자가 섞인 값을
-- 모두 "010-1234-1234" 로 치환합니다.
-- ※ '01012341234' 처럼 하이픈 없는 형식이 들어 있어도 그대로 두려면
--   아래 정규식의 '-' 를 그대로 두면 됩니다.
UPDATE reservations
SET phone = '010-1234-1234'
WHERE
  phone IS NULL
  OR btrim(phone) = ''
  OR btrim(phone) = '미입력'
  OR phone !~ '^[0-9+\-\s()]+$'      -- 숫자/+/-/공백/괄호 외 다른 글자 포함
  OR phone !~ '[0-9]';               -- 숫자가 아예 없는 경우

-- ============================================================
-- 2. 신청자 이름의 괄호 부분 제거
-- ============================================================
-- "홍길동(청년부)" → "홍길동"
-- "홍길동 (대표)" → "홍길동"
-- "(임시) 홍길동" → "홍길동"
-- 양 끝 공백, 중복 공백도 함께 정리합니다.
UPDATE reservations
SET guest_name = btrim(regexp_replace(guest_name, '\s*\([^)]*\)\s*', ' ', 'g'))
WHERE guest_name LIKE '%(%';

-- 중복 공백 제거 (위 치환 후 가운데 공백이 두 개 이상 남는 경우 대비)
UPDATE reservations
SET guest_name = regexp_replace(guest_name, '\s{2,}', ' ', 'g')
WHERE guest_name ~ '\s{2,}';

